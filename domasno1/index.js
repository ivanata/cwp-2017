function c2t(c){
	console.log(c*1.8+32 + ' ' + "F");
}
c2t(70);
c2t(30);

function f2c(n){
	console.log((n-32)/1.8 + ' ' + 'C');
}
f2c(60);
f2c(45);

function temp1(a){
 	console.log(a + ' C'+ ' ' + (a*1.8+32) + ' '+'F')
 }
 temp1(20);
 temp1(52);

